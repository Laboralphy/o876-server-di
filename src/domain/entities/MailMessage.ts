import z from 'zod';
import { EntityId } from '../schemas/EntityId';
import { String1k } from '../schemas/String1k';

export const MailMessageSchema = z.object({
    id: EntityId,
    senderId: EntityId,
    recipientIds: z.array(EntityId),
    topic: String1k,
    content: z.string(),
    tsCreation: z.number(),
});

export type MailMessage = z.infer<typeof MailMessageSchema>;

